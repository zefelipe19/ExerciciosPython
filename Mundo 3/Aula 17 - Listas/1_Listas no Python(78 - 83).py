'''Difirente das Tuplas que são imutáveis, as Listas no python podem ser mudadas, alem disso pode-se 
criar listas vazias e adcionar valores a essa lista, fazendo assim, sempre que um novo item for adcionado a lista
o python vai atualizar os indicies dessa lista, pois podemos adicionar e remover itens em diferentes posições.'''

''' Comando para manipulação e listas:
# Para adicionar novos itens as listas, os comandos são: 
.append()  Esse comando vai criar um novo ultimo indice e adicionar o paremêtro definido dentro dos parenteses.
.insert()  Esse comando vai inserir o item no indice escolhido, mudando itens posteriores de lugar caso necessário.

# Para remover itens das listas, os comandos são:
del {#Nome da lista}  Remove um item da lista baseado na posição indicada;
.remove()  Remove um item baseado no seu valor e não na sua posição;
.pop()  Remove da lista_compras o último item, mas não o exclui.'''
